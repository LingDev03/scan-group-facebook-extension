export function parseISODate(dateStr: string): Date {
  const [year, month, day] = dateStr.split('-').map(Number);
  return new Date(year, month - 1, day);
}

export function startOfDay(date: Date): Date {
  const d = new Date(date);
  d.setHours(0, 0, 0, 0);
  return d;
}

export function isPostAfterDate(createdAt: Date, scanAfterDate?: string): boolean {
  if (!scanAfterDate) return true;
  const cutoff = startOfDay(parseISODate(scanAfterDate));
  return createdAt >= cutoff;
}

export function parsePostDate(value: unknown): Date | null {
  if (value == null) return null;
  if (typeof value === 'number') {
    const ms = value < 1e12 ? value * 1000 : value;
    const d = new Date(ms);
    return Number.isNaN(d.getTime()) ? null : d;
  }
  if (typeof value === 'string') {
    const d = new Date(value);
    return Number.isNaN(d.getTime()) ? null : d;
  }
  return null;
}
